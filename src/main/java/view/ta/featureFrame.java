    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try {
        // 🔌 로그아웃 요청 전송
        out.writeObject("logout");
        out.flush();

        System.out.println("🔌 로그아웃 요청 전송 완료");

        // ✅ 기존 스트림과 소켓 닫기
        in.close();
        out.close();
        socket.close();
    } catch (IOException e) {
        e.printStackTrace();
    }

    try {
        // ✅ 새 소켓과 스트림 생성
        Socket newSocket = new Socket("localhost", 12345); // 실제 서버 주소/포트로
        ObjectOutputStream newOut = new ObjectOutputStream(newSocket.getOutputStream());
        ObjectInputStream newIn = new ObjectInputStream(newSocket.getInputStream());

        // ✅ 로그인 뷰 실행
        ServerConnector connector = new ServerConnector("localhost", 12345);
        AuthController controller = new AuthController(connector);
        new LoginView(controller, newSocket, newOut, newIn).setVisible(true);

        this.dispose();
    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "서버 재연결 실패");
    }
    }//GEN-LAST:event_jButton4ActionPerformed

